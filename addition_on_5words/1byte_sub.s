		AREA ONE_B,CODE,READONLY
		ENTRY
		MOV R5,#5
		MOV R6,#1
		LDR R2,=ARM
		LDR R1,=AM
		LDR R0,=A
SS		LDRB R3,[R2],#1
		LDRB R4,[R1],#1
		SUBS R3,R4
		STRB R3,[R0],#1
		SUBS R5,R6
		BNE SS
LOOP	B LOOP
ARM		DCB 0X01,0X02,0X03,0X04,0X05
AM		DCB 0X00,0X01,0X02,0X03,0X04
		AREA ONB,DATA,READWRITE
A		DCB 0
		END
