			AREA AD,CODE,READONLY
			ENTRY
			MOV R5,#5
			MOV R6,#1
			LDR R1,=ARM_STATE
			LDR R0,=ARM_ST
			LDR R2,=AM
BK			LDR R3,[R1],#4
			LDR R4,[R0],#4
			SUBS R3,R4
			STR	R3,[R2],#4
			SUBS R5,R6
			BNE BK
LOOP		B	LOOP
ARM_STATE	DCD 0X00000001,0X00000002,0X00000003,0X00000004,0X00000005
ARM_ST		DCD	0X00000001,0X00000001,0X00000001,0X00000001,0X00000001
			AREA SAMP,DATA,READWRITE
AM			DCD	0
			END
